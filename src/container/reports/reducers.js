// 获取ECharts option
function getOption(details) {
    // 接口获取的原始数据
    // console.log(details);
    // const chartType = details.reportType;
    // todo 那个字段
    const data = details.rows;
    const xLabel = details.axisXLabel;
    const xData = data.map((daily) => {
        return daily[xLabel];
    });

    //  legend
    const yLabel = details.axisYLabel;
    let legendData = yLabel.split(";");
    let legendSelected = {};

    // series
    let series = [];

    // todo
    const seriesItemType = "bar";

    legendData.forEach((legendName, index) => {
        // TODO 默认选中第一项
        if (index === 0) {
            legendSelected[legendName] = true;
        } else {
            legendSelected[legendName] = false;
        }


        let seriesItem = {};
        seriesItem.name = legendName;
        seriesItem.type = seriesItemType;
        seriesItem.data = data.map((daily) => {
            return daily[legendName];
        });
        series.push(seriesItem);
    });

    //获取x轴标签间隔多少显示
    let interval = 0;
    if (xData.length > 15) {
        interval = xData.length / 12;
    } else {
        interval = -1;
    }
    //四舍五入
    interval = Math.round(interval);

    return {
        grid: {
            show: false,
            left: '1%',
            right: "5%",
            top: '2%',
            bottom: "2%",
            // width: "80%",
            height: "80%",
            containLabel: true
        },
        // 设备背影色为白色, 透明度:0.1
        tooltip: {
            trigger: 'axis'
        },
        toolbox: {
            show: true
        },
        legend: {
            show: false,
            selected: legendSelected,
            data: legendData,
        },
        xAxis: {
            data: xData,
            axisLabel: {
                interval: interval,
                /*45度角倾斜显示*/
                rotate: 45
            }
        },
        yAxis: {
            //min: 'dataMin', /*将数据最小值作为开始值*/
            axisLabel: {
                formatter: function (value) {
                    return value
                }
            }
        },
        series: series,
        color: ['#c23531','#2f4554', '#61a0a8', '#d48265', '#91c7ae','#749f83',  '#ca8622', '#bda29a','#6e7074', '#546570', '#c4ccd3']
    };


}
const initialState = getOption(Details);

const chartRender= (state = initialState, action) => {
    switch (action.type) {
        case LEGEND_CHANGE:
            // todo
            const newOption = Object.assign({}, state);
            Object.assign(newOption.legend.selected, action.itemInfo);
            return newOption;

        default:
            return state
    }
};
export default chartRender;
